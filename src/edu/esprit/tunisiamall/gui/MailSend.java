    private void sendbtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sendbtActionPerformed
        // TODO add your handling code here:
        
        Properties props = new Properties();
        props.put("mail.smtp.host", "smtp.gmail.com");
        props.put("mail.smtp.socketFactory.port", "587");
        props.put("mail.smtp.socketFactory.class", "javax.net.ssl.SSLSocketFactory");
        props.put("mail.smtp.auth", "true");
        props.put("mail.smtp.port", "587");
        
        
        Session session= Session.getInstance(props,
             new javax.mail.Authenticator() {
                 protected PasswordAuthentication getPasswordAuthentication(){
                 return new PasswordAuthentication("mohamedali.boughanmi@esprit.tn", "dalio456");
                   
                 } 
             
             }
              
                );
        
       try{
            
           Message message = new MimeMessage (session);
           message.setFrom(new InternetAddress(Desttxt.getText()) );
           message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(Desttxt.getText()));
           message.setSubject("Test Mail");
           message.setText(Mailtext.getText());
           props.put("mail.smtp.starttls.enable", "true");
           Transport.send(message);
           JOptionPane.showMessageDialog(null, "Mail envoy√©");
           
       }
       catch(Exception e){
           
           JOptionPane.showMessageDialog(null, e);
       }
       
       
       this.setVisible(false);
        Formulairresp Resp = new Formulairresp();
        Resp.setVisible(true);
        
        
        
        
        
        
        
    }//GEN-LAST:event_sendbtActionPerformed

