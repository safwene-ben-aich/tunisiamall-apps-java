      public void DessinerBesoinDaide(String mot, int sleep){
          
          for (int i=0;i<mot.length();i++){
              try {
                  String word ="";
                  for (int j=0;j<=i;j++){
                      word += mot.charAt(j);
                  }
                  this.HelpjLabel.setText(word);
                  
                  Thread.sleep(sleep);
              } catch (InterruptedException ex) {
                  Logger.getLogger(LoginChoiseGUI.class.getName()).log(Level.SEVERE, null, ex);
              }
              
          }
            
      }
    
    
    
    

