    public void populateJTable(){
       
        ResponsableEnseigneDAO rdao= new ResponsableEnseigneDAO();
        ResponsableEnseigne r= new ResponsableEnseigne();
        MarqueDao mdao= new MarqueDao();
        Marque m = new Marque();
        
        
        
        List<ResponsableEnseigne> list =rdao.Display();
        
        String[] columnName = {"Nom","Prénom","Marque","Email"};
        Object[][] rows = new Object[list.size()][4];
        for(int i = 0; i < list.size(); i++){
            //System.out.println("**************"+list.get(i).getId_local());
            
            m.setNom(mdao.getNameMarqueByResponsable(list.get(i).getId()));
            //System.out.println("**************"+m.getNom());
            
            rows[i][0] = list.get(i).getNom();
            rows[i][1] = list.get(i).getPrenom();
            rows[i][2] = m.getNom();
            rows[i][3] = list.get(i).getMail();
              
        }
        
        ModelStatAdmin model = new ModelStatAdmin(rows, columnName);
        TableResponsable.setModel(model);
        TableResponsable.setRowHeight(80);
        TableResponsable.getColumnModel().getColumn(2).setPreferredWidth(100);
    }
    
    
